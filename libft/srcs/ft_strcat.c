char	*ft_strcat(char *dest, char *src)
{
	int a;

	a = 0;
	while (dest[a] != '\0')
		a++;
	while (*src)
	{
		dest[a] = *src;
		a++;
		++src;
	}
	dest[a] = '\0';
	return (dest);
}
