#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*p;
	size_t		i;

	p = (const char *)s;
	i = 0;
	while (i < n)
	{
		if (p[i] == (const char)c)
			return ((void *)((size_t)p + i));
		i++;
	}
	return (NULL);
}
