#include "libft.h"
#include <unistd.h>
#include <limits.h>

void	ft_putnbr_long(long n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + 48);
}
