int	ft_strequ(char const *s1, char const *s2)
{
	int	cmp;

	cmp = 0;
	if (!(s1 && s2))
		return (0);
	while (s1[cmp] == s2[cmp] && (s1[cmp] != '\0' || s2[cmp] != '\0'))
		cmp++;
	if (s1[cmp] == s2[cmp])
		return (1);
	return (0);
}
